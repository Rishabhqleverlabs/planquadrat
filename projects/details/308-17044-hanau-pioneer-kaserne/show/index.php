                    <p>The pioneer barracks in its basic structure is a testament to an eventful military and political history. It is situated like an island on the edge of the city at the transition to the landscape zone. Its radial system is due to the functional requirements as well as being an expression of national-socialist sentiment and military optimisation. All buildings, all utilisations are concentrated hierarchically on the centre. &nbsp;
                    </p>
                    <p>Transforming such a conversion area into a positively connoted residential location is equally a challenge and an opportunity due to the radicalism of the existing structure. &nbsp;
                    </p>
                    <p>The urban design therefore takes up the existing basic spatial elements of the barracks, such as central axis, the radial structure, existing buildings and newly defines them. In this respect the central axis is maintained as a succinct link route, but expressively overwritten by the new park complex. Accumulating along the old central axis and link route are now the entrance to the quarter, park and neighbourhood square, leading the user to the landscape zone via these open spaces. &nbsp; &nbsp;
                    </p>
                    <p>The time of the old structure is over even if the attentive observer can still perceive it. The hands of the radial system on the clock face have moved on and are now anchored in a different social and spatial context without denying the past. The Pioneer Park places the focus on the east-west connection. The new basic structure allows for narrative-spatial breaks and through this creates new and old in an oscillating balance that uses the opportunities. In the west the Pioneer Park faces the direction of the inner city and spreads out in the east to the transition to the landscape zone.&nbsp;
                    </p>
                    <p>The entrance to the quarter as a green square has the task of illustrating this new spirit and developing a space designed and enclosed by existing buildings and new buildings. Three quarters are being developed from the former barracks: each with its own identity but unified in the spatial image. &nbsp;
                    </p>
                    <p>The open space interweaves the three quarters with the surroundings without lapsing into the old formulas of the marching grounds. The green space, the park, the delicately linked pathways, the tree-lined streets now interweave the former barracks with the surrounding city and landscape zone and create excellent location quality for the future utilisations. &nbsp;&nbsp;
                    </p>
                    <p>Existing buildings such as the former canteen, the church, the 10 brothers are being converted and excitingly spatially staged along the park to become the core of the new quarter like the heating system.&nbsp;
