                    <h4>Conversion OF A FORMER RAILWAY SITE WITH LISTED EXITING BUILDINGS&nbsp;</h4>
                    <p>In the science city of Darmstadt the demand for living space remains undiminished due to the increase in population. For this reason the focus is on conversion areas close to the city. This includes, among others, the former railway site on Michaelisstraße.
                        <br />The area with several listed barracks buildings from the time of the first world war is situated on the western edge of the city of Darmstadt directly next to the forest and is being converted into a residential area under the marketing name of DAcore.
                        <br />The new residential quarter provides for a scattered development comprising six apartment &nbsp;buildings as well as five terraced houses and integrates the listed buildings in the newly developed urban concept. These partly originate from the time of the first world war when there were barracks here for aeronauts and radio operators, the other parts from the 1950s. &nbsp;
                        <br />The three-storey apartment buildings designed by planquadrat with penthouse level are characterised by an elegant and plain architectural language. The cubic structure with the white facade surfaces are divided up by spacious loggias and horizontal window hinges and have roof terraces that are cut out of the entire shape. The colours are conveyed in the new buildings between the former barracks buildings on Michaelis, Koblenzer Straße and the existing buildings in the area in order to emphasise the urban concept. &nbsp;
                        <br />The former riding and vehicle hall from the time of the first world war portrays an important element in the new order of the quarter in terms of urban design. It remains unchanged in its appearance and geometry and will also have the use of a park in the form of open parking decks for the residents in future. This solution approach enables an open view over the delicate listed roof construction and reduces additional ground sealing in the quarter.&nbsp;
                        <br />A further quotation of the current use will be integrating the vehicle hall from the 1950s in the outside facilities. The cantilever with the wide projecting steel supports will serve as roofing in the future for seating for the residents. &nbsp;&nbsp;
